class Solution:
    def largestUniqueNumber(self, A: List[int]) -> int:
        c = collections.Counter(A)
        l = sorted(list(c.items()), key=lambda x: (x[1], -x[0]))
        return -1 if l[0][1] > 1 else l[0][0]
